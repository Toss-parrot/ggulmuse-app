export function getStockById(id: string): Stock | undefined {
  return stocks.find((s) => s.id === id);
}

export function getPriceAtDate(stockId: string, date: string): number | undefined {
  const history = priceHistory[stockId];
  if (!history) return undefined;
  const point = history.find((p) => p.date === date);
  return point?.price;
}

export function getTeaser(stockId: string): string {
  const history = priceHistory[stockId];
  if (!history || history.length < 60) return '???';
  const fiveYearsAgo = history[history.length - 61];
  const now = history[history.length - 1];
  if (!fiveYearsAgo || !now) return '???';
  const ratio = now.price / fiveYearsAgo.price;
  const result = Math.round(100 * ratio);
  return `${result}만원`;
}
